package basics;

public class Operators {

	public static void main(String[] args) {
		/*
		 * 1.arthemeatic operators :- +,-,/,*,%,++,--
		 * 2.Logical Operators :- &&,||
		 * 3.Relational Operators :- >,<,<=,>=,==,!=
		 */
		
		int i=10;
	System.out.println(i/5); //2..quotient value
	System.out.println(i%5); //0..remainder
	i++; //i=i+1;
	System.out.println(i);
	i--; //i=i-1;
	System.out.println(i);
	int j=20;
	System.out.println(i>j); //false
	System.out.println(i<j); //true
	System.out.println(i>=j); //False
	System.out.println(i<=j); //true
	System.out.println(i!=j); //true

	}
	


}
